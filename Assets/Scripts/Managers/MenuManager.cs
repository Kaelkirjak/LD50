using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using System;

public enum MenuPanel
{

}

public class MenuManager : MonoBehaviour
{
    /// ---------------------------------------------------------------------------------------------------------------------------------------------
    /// ---------------------------------------------------------------------------------------------------------------------------------------------
    /// ---------------------------------------------------------------------------------------------------------------------------------------------

    #region Events

    public event Action<MenuPanel> onPanelChanged;

    #endregion Events

    /// ---------------------------------------------------------------------------------------------------------------------------------------------
    /// ---------------------------------------------------------------------------------------------------------------------------------------------
    /// ---------------------------------------------------------------------------------------------------------------------------------------------

    #region Menu Functions

    /// <summary>
    /// Change the menu panel
    /// </summary>
    /// <param name="panelToChangeTo">Panel to change to</param>
    public void ChangePanel(MenuPanel panelToChangeTo)
    {
        onPanelChanged?.Invoke(panelToChangeTo);
    }

    #endregion Menu Functions

    /// ---------------------------------------------------------------------------------------------------------------------------------------------
    /// ---------------------------------------------------------------------------------------------------------------------------------------------
    /// ---------------------------------------------------------------------------------------------------------------------------------------------

    #region General Functions

    private void Awake()
    {
        InstanceAwake();
    }

    #endregion General Functions

    #region Singletons

    public static MenuManager instance;

    void InstanceAwake()
    {
        if(instance == null)
        {
            instance = this;
        }
        else
        {
            Destroy(gameObject);
        }
    }

    #endregion Singletons

    /// ---------------------------------------------------------------------------------------------------------------------------------------------
    /// ---------------------------------------------------------------------------------------------------------------------------------------------
    /// ---------------------------------------------------------------------------------------------------------------------------------------------
}
